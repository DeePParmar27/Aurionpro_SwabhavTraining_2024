package com.aurionpro.model;

public class ArithmaticOperator {
	public static void main(String[] args) {
		int Number1 = 10 ;
		int Number2 = 20 ;
		
		System.out.println("This is + operator"+(Number1+Number2));
		System.out.println("This is - operator"+(Number1-Number2));
		System.out.println("This is * operator"+(Number1*Number2));
		System.out.println("This is / operator"+(Number1/Number2));
		System.out.println("This is + operator"+(Number1%Number2));
		System.out.println("This is ++ operator"+(++Number1));
		System.out.println("This is -- operator"+(--Number2));






	}

}
